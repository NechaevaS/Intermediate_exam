int	is_anagram(char *a, char *b)
{
	int arr[127] = {0};
	int i = 0;

	if (!a || !b)
		return (0);
	while (a[i] && b[i])
	{
		arr[a[i] - 32]++;
		arr[b[i] - 32]--;
		i++;
	}
	i = 0;
	while (i < 127)
	{
		if (arr[i] != 0)
			return (0);
		i++;
	}
	return (1);
}
